package support;

public class degreeInfo {
	
	public degreeInfo(String schoolID, String sName, String disci, String gpa, String degree, String degree_time)
	{
		sID = schoolID;
		school_name = sName;
		discipline = disci;
		GPA = gpa;
		this.degree = degree;
		this.degree_time = degree_time;
	}
	public  String sID;
	public  String school_name;
	public  String discipline;
	public  String GPA;
	public  String degree;
	public  String degree_time;
}
